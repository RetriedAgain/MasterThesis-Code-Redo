package de.masterzoo;

public class Secret {
    private int i;
    private final int j;

    public Secret(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public void setI(int i) {
        this.i = i;
    }
}
